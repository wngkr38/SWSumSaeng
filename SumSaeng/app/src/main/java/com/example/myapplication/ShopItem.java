package com.example.myapplication;

public class ShopItem {
    private String itemimage;
    private String itemplace;
    private String itemname;
    private int itempoint;

    public ShopItem() {
    }

    public String getItemImage() {return itemimage;}

    public void setItemImage(String itemimage) {this.itemimage = itemimage;}

    public String getItemPlace() {return itemplace;}

    public void setItemPlace(String itemplace) {this.itemplace = itemplace;}

    public String getItemName() {return itemname;}

    public void setItemName(String itemname) {this.itemname = itemname;}

    public int getItemPoint() {return itempoint;}

    public void setItemPoint(int itempoint) {this.itempoint = itempoint;}
}


